namespace WsRpcServer.Events;

/// <summary>
/// Універсальна модель для JSON-RPC сповіщень.
/// Представляє структуру сповіщення, яке відправляється клієнтам.
/// </summary>
/// <param name="Method">Назва методу сповіщення, який буде викликаний на клієнтській стороні.</param>
/// <param name="Arguments">Масив аргументів, що передаються методу сповіщення.</param>
/// <remarks>
/// Використання record типу замість класу надає переваги:
/// 1. Неявна реалізація конструктора з усіма параметрами
/// 2. Автоматична реалізація методів Equals, GetHashCode, ToString
/// 3. Незмінність (immutability) - важлива властивість для передачі повідомлень
/// 
/// Сповіщення використовується в контексті JSON-RPC, де метод представляє 
/// назву сповіщення, а аргументи передаються як параметри цього методу.
/// 
/// Незмінність об'єкта (immutability) забезпечує безпечну передачу сповіщень 
/// між різними компонентами системи без ризику зміни стану.
/// </remarks>
public record RpcNotification(string Method, object[] Arguments);