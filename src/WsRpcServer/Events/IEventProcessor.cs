namespace WsRpcServer.Events;

/// <summary>
/// Інтерфейс для обробників подій.
/// Визначає контракт для компонентів, які обробляють події системи та перетворюють їх на сповіщення для клієнтів.
/// </summary>
/// <remarks>
/// Цей інтерфейс є ключовим компонентом архітектури, що забезпечує реактивну взаємодію між сервером та клієнтами.
/// Він виступає як посередник, який отримує події від джерел даних (наприклад, баз даних, черг повідомлень, тощо)
/// і перетворює їх на JSON-RPC сповіщення для відправки клієнтам.
/// 
/// Реалізації цього інтерфейсу можуть використовувати різні стратегії для обробки подій:
/// - Опитування зовнішніх сервісів (polling)
/// - Підписки на черги повідомлень (message queues)
/// - Потоки подій (event streams)
/// - Вебхуки (webhooks)
/// 
/// Кожен клієнт реєструється для отримання подій через метод RegisterClient, що дозволяє
/// обробнику подій знати, куди направляти сповіщення.
/// </remarks>
public interface IEventProcessor
{
    /// <summary>
    /// Запускає процес обробки подій.
    /// </summary>
    /// <param name="cancellationToken">Токен скасування для зупинки обробки подій.</param>
    /// <returns>Завдання, яке представляє асинхронну операцію запуску.</returns>
    /// <remarks>
    /// Цей метод має ініціювати підключення до джерел подій та почати їх обробку.
    /// Зазвичай він запускає фонові задачі, які працюють до виклику StopAsync або скасування токена.
    /// </remarks>
    Task StartAsync(CancellationToken cancellationToken);

    /// <summary>
    /// Зупиняє процес обробки подій.
    /// </summary>
    /// <param name="cancellationToken">Токен скасування для контролю процесу зупинки.</param>
    /// <returns>Завдання, яке представляє асинхронну операцію зупинки.</returns>
    /// <remarks>
    /// Цей метод має коректно зупинити всі фонові процеси, від'єднатися від джерел подій,
    /// і звільнити ресурси. Важливо реалізувати цей метод з урахуванням можливих таймаутів,
    /// щоб уникнути "зависання" при зупинці сервера.
    /// </remarks>
    Task StopAsync(CancellationToken cancellationToken);

    /// <summary>
    /// Реєструє клієнта для отримання сповіщень про події.
    /// </summary>
    /// <param name="clientId">Унікальний ідентифікатор клієнта.</param>
    /// <param name="notificationHandler">Функція обробник, яка буде викликана для відправки сповіщень клієнту.</param>
    /// <remarks>
    /// Функція notificationHandler приймає два параметри:
    /// - Назва методу сповіщення (string)
    /// - Масив аргументів сповіщення (object[])
    /// 
    /// Ця функція є мостом між обробником подій та клієнтською сесією. Вона дозволяє
    /// обробнику подій відправляти сповіщення клієнту, не знаючи деталей реалізації
    /// транспортного рівня (WebSocket, TCP, тощо).
    /// 
    /// Зазвичай ця функція реалізована як метод у класі сесії, який викликає JsonRpc.NotifyAsync.
    /// </remarks>
    void RegisterClient(Guid clientId, Func<string, object[], Task> notificationHandler);

    /// <summary>
    /// Скасовує реєстрацію клієнта та припиняє надсилання йому сповіщень.
    /// </summary>
    /// <param name="clientId">Унікальний ідентифікатор клієнта.</param>
    /// <remarks>
    /// Цей метод має видалити всі підписки та посилання на клієнта, щоб запобігти
    /// витоку пам'яті та відправці сповіщень відключеним клієнтам.
    /// 
    /// Важливий аспект ефективного управління ресурсами сервера.
    /// </remarks>
    void UnregisterClient(Guid clientId);
}