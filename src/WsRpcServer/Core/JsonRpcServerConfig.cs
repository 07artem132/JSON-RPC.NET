using System.ComponentModel.DataAnnotations;

namespace WsRpcServer.Core;

/// <summary>
/// Конфігурація для JSON-RPC сервера.
/// Використовує record тип для автоматичного створення конструктора та властивостей.
/// </summary>
/// <remarks>
/// Використання record типу замість звичайного класу надає кілька переваг:
/// 1. Неявна реалізація конструктора з усіма властивостями
/// 2. Автоматична реалізація методів Equals, GetHashCode, Clone, тощо
/// 3. Неявна реалізація деконструктора
/// 4. Підтримка інших функцій records, як-от with-expressions
/// 
/// Це особливо зручно для конфігураційних об'єктів, які часто потребують порівняння
/// та клонування.
/// </remarks>
public record JsonRpcServerConfig
{
    /// <summary>
    /// Адреса хоста для прослуховування.
    /// За замовчуванням "0.0.0.0", що означає прослуховування на всіх доступних інтерфейсах.
    /// </summary>
    /// <remarks>
    /// Використання "0.0.0.0" дозволяє серверу приймати з'єднання на всіх мережевих інтерфейсах.
    /// Для безпеки у виробничому середовищі можна обмежити до конкретного інтерфейсу.
    /// </remarks>
    [Required(AllowEmptyStrings = false)]
    public string Host { get; set; } = "0.0.0.0";

    /// <summary>
    /// Порт для прослуховування.
    /// За замовчуванням 9000, що є нестандартним портом, щоб уникнути конфліктів.
    /// </summary>
    /// <remarks>
    /// Використання порту нижче 1024 зазвичай потребує підвищених привілеїв у Unix-подібних системах.
    /// Порт 9000 є достатньо високим для використання без спеціальних привілеїв.
    /// </remarks>
    [Range(1, 65535)]
    public int Port { get; set; } = 9000;

    /// <summary>
    /// Максимальний розмір повідомлення в байтах.
    /// За замовчуванням 100 МБ, що є достатньо великим для більшості випадків.
    /// </summary>
    /// <remarks>
    /// Це обмеження важливе для захисту від атак типу "бомба JSON" або неконтрольованого 
    /// споживання пам'яті. Значення встановлено досить високим для підтримки великих повідомлень,
    /// але в реальних сценаріях може бути зменшене.
    /// </remarks>
    [Range(1, int.MaxValue)]
    public int MaxMessageSizeBytes { get; set; } = 100 * 1024 * 1024;

    /// <summary>
    /// Розмір черги сповіщень для кожного клієнта.
    /// За замовчуванням 1000, що дозволяє буферизувати багато сповіщень перед відкиданням старих.
    /// </summary>
    /// <remarks>
    /// Використовується з BoundedChannelFullMode.DropOldest для реалізації механізму
    /// противотиску (backpressure). Якщо клієнт не встигає обробляти сповіщення,
    /// найстаріші будуть відкидатися для запобігання переповненню пам'яті.
    /// </remarks>
    [Range(1, int.MaxValue)]
    public int NotificationQueueSize { get; set; } = 1000;

    /// <summary>
    /// Поріг для призупинки запису у канал (у байтах).
    /// За замовчуванням 1 МБ, що запобігає надмірному споживанню пам'яті при передачі даних.
    /// </summary>
    /// <remarks>
    /// Використовується з System.IO.Pipelines для контролю потоку даних.
    /// Коли буфер запису перевищує цей поріг, запис призупиняється до того моменту,
    /// коли читач обробить частину даних, що зменшує ризик вичерпання пам'яті.
    /// </remarks>
    [Range(1, int.MaxValue)]
    public int PipeThresholdBytes { get; set; } = 1024 * 1024;

    /// <summary>
    /// Таймаут для операцій сповіщення.
    /// За замовчуванням 5 секунд, що є балансом між надійністю та швидкодією.
    /// </summary>
    /// <remarks>
    /// Цей таймаут використовується для запобігання блокування потоків при надсиланні сповіщень.
    /// Якщо клієнт не може прийняти сповіщення протягом цього часу, операція скасовується,
    /// що запобігає витоку ресурсів при проблемних клієнтах.
    /// </remarks>
    public TimeSpan NotificationTimeout { get; set; } = TimeSpan.FromSeconds(5);

    /// <summary>
    /// Максимальна кількість послідовних помилок розбору JSON, після якої з'єднання примусово закривається.
    /// За замовчуванням 10.
    /// </summary>
    /// <remarks>
    /// Захист від DoS-вектора: зловмисник може надсилати потік невалідного JSON, змушуючи сервер
    /// нескінченно виконувати цикл відновлення (rent буфера + лінійний скан) і насичувати CPU на одному
    /// з'єднанні. Після цієї кількості послідовних невдалих розборів з'єднання закривається зі статусом
    /// <see cref="System.Net.WebSockets.WebSocketCloseStatus.ProtocolError"/>. Лічильник скидається після
    /// кожного успішного розбору повідомлення.
    /// </remarks>
    [Range(1, int.MaxValue)]
    public int MaxConsecutiveParseFailures { get; set; } = 10;
}