using System.Net.WebSockets;

namespace WsRpcServer.Sessions;

/// <summary>
/// Інтерфейс для JSON-RPC сесій.
/// Визначає контракт для клієнтських сесій, які використовують JSON-RPC через WebSocket.
/// </summary>
/// <remarks>
/// Цей інтерфейс абстрагує деталі WebSocket комунікації та надає високорівневий API
/// для роботи з JSON-RPC сесіями. Він діє як міст між транспортним рівнем (WebSocket)
/// та рівнем протоколу (JSON-RPC).
/// 
/// Кожна сесія представляє одного підключеного клієнта і забезпечує методи для
/// надсилання йому сповіщень та керування з'єднанням.
/// </remarks>
public interface IJsonRpcSession
{
    /// <summary>
    /// Унікальний ідентифікатор сесії.
    /// </summary>
    /// <remarks>
    /// Використовується для ідентифікації клієнта в системі підписок та обробників подій.
    /// Guid обраний як тип ідентифікатора для забезпечення глобальної унікальності 
    /// та відсутності колізій навіть у розподіленому середовищі.
    /// </remarks>
    Guid Id { get; }

    /// <summary>
    /// Відправляє сповіщення клієнту.
    /// </summary>
    /// <param name="method">Ім'я методу сповіщення.</param>
    /// <param name="args">Аргументи сповіщення.</param>
    /// <returns>Завдання, яке представляє асинхронну операцію відправки.</returns>
    /// <remarks>
    /// Цей метод формує та відправляє сповіщення JSON-RPC клієнту.
    /// Внутрішньо він використовує StreamJsonRpc для серіалізації та відправки сповіщення.
    /// 
    /// Параметри:
    /// - method: відповідає полю "method" у JSON-RPC сповіщенні
    /// - args: відповідає полю "params" у JSON-RPC сповіщенні
    /// 
    /// Метод має повертати Task, щоб забезпечити асинхронну роботу без блокування потоків.
    /// </remarks>
    Task SendNotificationAsync(string method, params object[] args);

    /// <summary>
    /// Відправляє бінарні дані клієнту.
    /// </summary>
    /// <param name="data">Бінарні дані для відправки.</param>
    /// <returns>Завдання, яке представляє асинхронну операцію відправки.</returns>
    /// <remarks>
    /// Цей метод дозволяє відправляти низькорівневі бінарні дані безпосередньо через WebSocket.
    /// Це може бути корисно для оптимізації передачі великих бінарних даних, оминаючи 
    /// накладні витрати на серіалізацію JSON.
    /// 
    /// Зазвичай використовується для спеціалізованих сценаріїв, таких як передача файлів,
    /// потокове відео або аудіо, тощо.
    /// </remarks>
    Task SendBinaryDataAsync(ReadOnlyMemory<byte> data);

    /// <summary>
    /// Закриває WebSocket з'єднання з вказаним статусом та причиною.
    /// </summary>
    /// <param name="status">Статус закриття WebSocket.</param>
    /// <param name="reason">Причина закриття з'єднання.</param>
    /// <remarks>
    /// Цей метод дозволяє коректно завершити WebSocket з'єднання, надаючи клієнту
    /// інформацію про причину закриття. Це важливо для правильної обробки закриття
    /// на стороні клієнта.
    /// 
    /// Статуси закриття визначені у специфікації WebSocket (RFC 6455) і включають:
    /// - 1000: Normal Closure (нормальне закриття)
    /// - 1001: Going Away (сервер відключається)
    /// - 1002: Protocol Error (помилка протоколу)
    /// - 1003: Unsupported Data (непідтримуваний тип даних)
    /// - ...і багато інших
    /// </remarks>
    void Close(WebSocketCloseStatus status, string reason);
}