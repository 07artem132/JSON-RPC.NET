namespace WsRpcServer.Services;

/// <summary>
/// Інтерфейс для клієнт-залежних RPC-сервісів.
/// Визначає сервіси, які потребують інформацію про клієнта, що викликає методи.
/// </summary>
/// <remarks>
/// Цей інтерфейс наслідується від IRpcService і використовується для ідентифікації
/// сервісів, які потребують контексту клієнта для своєї роботи.
/// 
/// Клієнт-залежні сервіси відрізняються від звичайних RPC-сервісів тим, що:
/// 1. Вони створюються для кожного клієнта окремо
/// 2. Приймають ідентифікатор клієнта (Guid clientId) через конструктор
/// 3. Можуть зберігати стан конкретного клієнта
/// 
/// При реєстрації таких сервісів, система RpcServiceRegistry автоматично передає
/// ідентифікатор клієнта в конструктор, що дозволяє сервісу знати, який клієнт
/// викликає його методи.
/// 
/// Приклад використання:
/// ```csharp
/// public class UserService : IClientAwareRpcService
/// {
///     private readonly Guid _clientId;
///     
///     public UserService(Guid clientId)
///     {
///         _clientId = clientId;
///     }
///     
///     public Task<UserInfo> GetCurrentUser()
///     {
///         // Використання _clientId для ідентифікації користувача
///     }
/// }
/// ```
/// </remarks>
public interface IClientAwareRpcService : IRpcService
{
}