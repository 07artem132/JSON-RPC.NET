using StreamJsonRpc;

namespace WsRpcServer.Services;

/// <summary>
/// Інтерфейс для реєстру RPC-сервісів.
/// Визначає контракт для компонента, який відповідає за реєстрацію RPC-сервісів у JSON-RPC системі.
/// </summary>
/// <remarks>
/// Цей інтерфейс є ключовим для інтеграції RPC-сервісів з StreamJsonRpc бібліотекою.
/// Він забезпечує механізм для автоматичного виявлення та реєстрації сервісів,
/// що дозволяє клієнтам викликати їх методи через JSON-RPC.
/// 
/// Основна відповідальність реєстру:
/// 1. Виявлення доступних RPC-сервісів у застосунку
/// 2. Реєстрація їх як цільових об'єктів (targets) у StreamJsonRpc
/// 3. Підтримка клієнт-залежних сервісів з передачею контексту клієнта
/// 
/// Цей підхід дозволяє розробникам просто додавати нові RPC-сервіси, 
/// без необхідності вручну реєструвати їх у системі.
/// </remarks>
public interface IRpcServiceRegistry
{
    /// <summary>
    /// Реєструє сервіси у екземплярі JSON-RPC.
    /// </summary>
    /// <param name="jsonRpc">Екземпляр JSON-RPC, в якому будуть зареєстровані сервіси.</param>
    /// <param name="clientId">Ідентифікатор клієнта, для якого реєструються сервіси.</param>
    /// <remarks>
    /// Цей метод викликається для кожного нового клієнтського з'єднання. Він має:
    /// 1. Знайти всі доступні RPC-сервіси
    /// 2. Отримати їх екземпляри з DI-контейнера (для звичайних сервісів)
    ///    або створити нові екземпляри з ідентифікатором клієнта (для клієнт-залежних)
    /// 3. Зареєструвати їх у наданому екземплярі JsonRpc
    /// 
    /// Для клієнт-залежних сервісів (IClientAwareRpcService) створюються нові
    /// екземпляри для кожного клієнта, щоб вони могли зберігати стан клієнта.
    /// 
    /// Внутрішньо використовує StreamJsonRpc.JsonRpc.AddLocalRpcTarget для реєстрації сервісів.
    /// </remarks>
    void RegisterServices(JsonRpc jsonRpc, Guid clientId);
}